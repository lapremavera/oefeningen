public class ForLoop {

    public static void main(String[] args) {

        int getal = 0;
        while (getal < 10) {

            System.out.println("Getal is " + getal);
            getal++;
        }

        for (int i = 0; i< 10; i++){
            System.out.println("Getal is "+ getal);
        }
    }
}
